# Variables
# It is nothing but memory location, which means when you create variable
# it reserves some memory location.


name = "Joe"  # str
age = 20  # int
course_subscribed = ["Python", "Unix", "Machine Language"]  # list
course_fee = 299.99  # float
student = (name, age, course_subscribed, course_fee)  # tuple

print(student)
