# My sample python program to show how indentation works?
def print_message(value):                        # 1st Level Indentation
    print("Checking Value passed in argument: ") # 2st Level Indentation
    if (value):                                  # 2st Level Indentation
        print("True")                            # 3rd Level Indentation
    else:                                        # 2st Level Indentation
        print("False")                           # 3rd Level Indentation


print_message(True)                              # 1st Level Indentation
